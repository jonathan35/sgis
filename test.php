Malaysian Sustainable Palm Oil (MSPO) -MSPO 2530:2013<br><br>

MSPO address sustainability issues and challenges in relation to the multi-stakeholders involved in the industry which complies with Malaysian laws and ratified international agreements. The standard describes the sustainability requirements for the production throughout the supply chain from the raw materials until it is transportedto the customer and makes it possible for smallholders to establish, maintain and improve their operational practices within a management system framework, which enables the approach towards attaining sustainable production of palm oil.
<br><br>
