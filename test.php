    <ul> 
    <li>Single tax deduction by the Inland Revenue Board of Malaysia for each accreditation programme that the CAB is accredited for.</li>
    <li>Double tax deduction by the Inland Revenue Board of Malaysia for organizations/companies to obtain accredited certification.</li>
